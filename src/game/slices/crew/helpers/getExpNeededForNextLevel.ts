/**
 * Вычисляет необходимое количество опыта для следующего уровня
 *
 * Формула: уровень * 100
 * Например:
 * - Уровень 1 → 100 опыта для уровня 2
 * - Уровень 2 → 200 опыта для уровня 3
 *
 * @param level - Текущий уровень члена экипажа
 * @returns Количество опыта, необходимое для следующего уровня
 */
export const getExpNeededForNextLevel = (level: number) => level * 100;
